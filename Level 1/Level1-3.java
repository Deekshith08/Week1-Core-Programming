//Create a program to convert the distance of 10.8 kilometers to miles.

class Main {
    public static void main(String[] args) {
        double givenKm=10.8;
        double kmtoMiles = 1.6;
        double finalMiles = givenKm/kmtoMiles;
        System.out.println( "The distance " + givenKm + " km in miles is " + finalMiles );
    }
}